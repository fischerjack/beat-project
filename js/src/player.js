class Player{

  constructor(startingXCoordinate, startingYCoordinate, ){
    this.xCoordinate = startingXCoordinate;
    this.yCoordinate = startingYCoordinate;
    this.width = 5;
    this.height = 5;
    this.speed = 3;
    this.xVelocity = 0;
    this.yVelocity = 0;
    this.jumping = false;
  }

  

}